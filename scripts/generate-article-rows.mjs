import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, "..");
const defaultCsvPath = path.join(rootDir, "data", "INITIAL_ARTICLES.csv");
const googleDriveCsvPath =
  "/Users/kensuke_kawamura/Library/CloudStorage/GoogleDrive-kensuke5704@gmail.com/マイドライブ/(76)media/INITIAL_ARTICLES.csv";
const csvPath =
  process.env.ARTICLE_CSV_PATH ||
  (fs.existsSync(googleDriveCsvPath) ? googleDriveCsvPath : defaultCsvPath);
const outPath = path.join(rootDir, "lib", "articleRows.generated.ts");

function parseCsv(text) {
  const rows = [];
  let row = [];
  let cell = "";
  let inQuotes = false;

  for (let index = 0; index < text.length; index += 1) {
    const char = text[index];
    const next = text[index + 1];

    if (char === '"' && inQuotes && next === '"') {
      cell += '"';
      index += 1;
      continue;
    }

    if (char === '"') {
      inQuotes = !inQuotes;
      continue;
    }

    if (char === "," && !inQuotes) {
      row.push(cell);
      cell = "";
      continue;
    }

    if ((char === "\n" || char === "\r") && !inQuotes) {
      if (char === "\r" && next === "\n") {
        index += 1;
      }
      row.push(cell);
      if (row.some((value) => value.trim() !== "")) {
        rows.push(row);
      }
      row = [];
      cell = "";
      continue;
    }

    cell += char;
  }

  if (cell !== "" || row.length > 0) {
    row.push(cell);
    if (row.some((value) => value.trim() !== "")) {
      rows.push(row);
    }
  }

  return rows;
}

function splitList(value) {
  return value
    .split(/[、,]/u)
    .map((item) => item.trim())
    .filter(Boolean);
}

function requireValue(record, key, rowNumber) {
  const value = record[key]?.trim();
  if (!value) {
    throw new Error(`Missing required value "${key}" at CSV row ${rowNumber}`);
  }
  return value;
}

const csvText = fs.readFileSync(csvPath, "utf8").replace(/^\uFEFF/u, "");
if (path.resolve(csvPath) !== path.resolve(defaultCsvPath)) {
  fs.copyFileSync(csvPath, defaultCsvPath);
}

const [headers, ...records] = parseCsv(csvText);
const headerKeys = headers.map((header) => header.trim());

const rows = records.map((record, index) => {
  const rowNumber = index + 2;
  const values = Object.fromEntries(headerKeys.map((key, keyIndex) => [key, record[keyIndex] ?? ""]));
  const no = Number.parseInt(requireValue(values, "No", rowNumber), 10);

  if (!Number.isInteger(no)) {
    throw new Error(`Invalid No at CSV row ${rowNumber}`);
  }

  return {
    no,
    category: requireValue(values, "category", rowNumber),
    title: requireValue(values, "title", rowNumber),
    slug: requireValue(values, "slug", rowNumber),
    characterName: values.characterName?.trim() || undefined,
    workName: values.workName?.trim() || undefined,
    mainColor: requireValue(values, "mainColor", rowNumber),
    subColor: requireValue(values, "subColor", rowNumber),
    accentColor: requireValue(values, "accentColor", rowNumber),
    moodKeywords: splitList(requireValue(values, "moodKeywords", rowNumber)),
    scenes: splitList(requireValue(values, "scene", rowNumber)),
    itemTypes: splitList(requireValue(values, "itemTypes", rowNumber)),
    affiliatePriority: splitList(requireValue(values, "affiliatePriority", rowNumber)),
    sourceStatus: requireValue(values, "status", rowNumber),
    memo: values.memo?.trim() || "",
  };
});

const output = `// This file is generated from ${path.relative(rootDir, csvPath)}.
// Run \`npm run articles:sync\` after editing the article CSV.
// Do not edit this file directly.

import type { ArticleSeed } from "./types";

export const articleRows = ${JSON.stringify(rows, null, 2)} satisfies ArticleSeed[];
`;

fs.writeFileSync(outPath, output);
console.log(`Generated ${path.relative(rootDir, outPath)} from ${csvPath}`);
